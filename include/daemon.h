/**
 * @file daemon.h
 * @brief 에디션 데몬 공개 인터페이스 (예약 헤더)
 *
 * ====================================================================
 *  파일 역할
 * ====================================================================
 *  에디션 데몬의 최상위 공개 인터페이스를 정의하기 위한 헤더이다.
 *  현재는 예약(placeholder) 상태이며, 데몬 초기화와 라이프사이클 API는
 *  include/purecvisor/core.h (PvContext, pv_init, pv_run, pv_cleanup)에서
 *  제공하고 있다.
 *
 * ====================================================================
 *  아키텍처 위치
 * ====================================================================
 *  공개 헤더(include/) 최상위에 위치한다.
 *  향후 데몬 전용 설정/시그널 핸들링/그레이스풀 셧다운 등
 *  core.h에 넣기 어려운 데몬 고유 API를 분리할 때 사용할 수 있다.
 *
 *  [include/ 디렉토리 구조]
 *    include/
 *      daemon.h          <-- 이 파일 (예약 — 향후 데몬 고유 API 분리용)
 *      core_state.h      <-- VM 오퍼레이션 잠금 상태 머신 (인메모리 GHashTable 버전)
 *      purecvisor/
 *        core.h          <-- 코어 라이프사이클 API (PvContext, pv_init/run/cleanup)
 *        server.h        <-- UDS 서버 API
 *        storage.h       <-- 스토리지 드라이버 API
 *        pcv_error.h     <-- GError 도메인/코드 정의
 *        pcv_validate.h  <-- 입력값 검증 API (VM 이름, CIDR, MAC 등)
 *
 *  [다른 모듈과의 관계]
 *    - include/purecvisor/core.h: 현재 데몬 진입점 API를 제공하는 파일.
 *      pv_init(), pv_run(), pv_cleanup()이 정의되어 있음.
 *    - src/core/daemon.c: core.h에 선언된 함수들의 구현 파일.
 *      레거시 경로이며, 현재는 main.c가 16단계 초기화를 직접 수행함.
 *    - src/main.c: 실제 데몬 진입점. daemon.c 대신 모든 서브시스템을 직접 초기화.
 *
 * ====================================================================
 *  향후 사용 계획
 * ====================================================================
 *  다음과 같은 데몬 전용 API를 이 헤더에 분리할 수 있다:
 *    - daemon_get_uptime(): 데몬 가동 시간 조회
 *    - daemon_get_mode(): NORMAL/DEGRADED/DRAIN 모드 조회
 *    - daemon_reload_config(): 런타임 설정 재로딩 (SIGHUP 핸들러)
 *    - daemon_get_version(): 빌드 버전/커밋 해시 조회
 *
 * ====================================================================
 *  주의사항
 * ====================================================================
 *  - 현재 빈 파일이다. 코드에서 include하는 곳이 없다.
 *  - 새 API 추가 시 반드시 include guard(#ifndef ... #define ... #endif)와
 *    G_BEGIN_DECLS/G_END_DECLS를 넣어 C++ 호환성을 유지할 것.
 *  - 이 파일에 API를 추가하면 Makefile의 헤더 의존성에도 반영해야 한다.
 */
